package example.switchs;

public class Switchs {
    public static void main(String[] args) {
        int option = 99;
        switch (option) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            default:
                System.out.println("default");
                break;
        }
    }
}
